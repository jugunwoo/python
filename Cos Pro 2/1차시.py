def 함수(선호티셔츠):
    티셔츠개수=[0,0,0,0,0,0] #처음에는 0으로 설정
                # xs,s,m,l,xl,xxl
    for 티셔츠 in 선호티셔츠: #선호티셔츠 하나씩 티셔츠에 넣기
        if 티셔츠=="XS":
            티셔츠개수[0]+=1
        if 티셔츠=="S":
            티셔츠개수[1]+=1
        if 티셔츠=="M":
            티셔츠개수[2]+=1
        if 티셔츠=="L":
            티셔츠개수[3]+=1
        if 티셔츠=="XL":
            티셔츠개수[4]+=1
        if 티셔츠=="XXL":
            티셔츠개수[5]+=1

    return 티셔츠개수 # 함수 끝내기=> 티셔츠 개수 변환
#학생들이 선호하는 티셔츠 목록
선호티셔츠=["S","S","XS","L","XXL","S","XL","L"]
# xs=1  s=3  m=0  l=2  xl=1  xxl=1
print("함수 실행 결과:",함수(선호티셔츠)) #함수 호출하기


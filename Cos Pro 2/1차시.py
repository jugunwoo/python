def 개수구하기(목록):
    홀수짝수개수=[0,0]

    for i in 목록:
        if i >=100 and i<1000:
            세자리수.append(i)

    for e in 세자리수:
        if e%2==1:
            홀수짝수개수[0]+=1
        else:
            홀수짝수개수[1]+=1
    return 홀수짝수개수
